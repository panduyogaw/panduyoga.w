<template>
    <div class="relative z-10">
      <label
        :for="id"
        class="block mb-2 text-sm font-medium text-gray-900 dark:text-gray-300"
      >
        {{ label }}
      </label>
      <component
        :is="type === 'textarea' ? 'textarea' : 'input'"
        :id="id"
        :type="type !== 'textarea' ? type : undefined"
        :rows="type === 'textarea' ? rows : undefined"
        :placeholder="placeholder"
        class="shadow-sm text-gray-900 text-sm rounded-lg block w-full p-2.5 focus:ring-primary-500 focus:border-primary-500 border border-secondary dark:bg-[#FBF8EF] bg-[#FBF8EF]"
        :aria-label="label"
        :value="modelValue"
        @input="$emit('update:modelValue', $event.target.value)"
      />
    </div>
  </template>
  
  <script setup>
  defineProps({
    id: { type: String, required: true },
    label: { type: String, required: true },
    type: { type: String, default: "text" },
    placeholder: { type: String, default: "" },
    rows: { type: Number, default: 4 },
    modelValue: String, // Pastikan menggunakan modelValue untuk v-model
  });
  
  defineEmits(["update:modelValue"]); // Emit perubahan nilai input
  </script>
  